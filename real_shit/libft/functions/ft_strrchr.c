#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	int		len;

	len = ft_strlen(s) + 1;
	i = len - 1;
	while (i >= 0)
	{
		if (s[i] == c)
			return ((char *) &s[i]);
		i--;
	}
	return (NULL);
}
