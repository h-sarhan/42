int	ft_isalnum(int c)
{
	int	is_alpha;
	int	is_num;

	is_alpha = (c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z');
	is_num = c >= '0' && c <= '9';
	return (is_alpha || is_num);
}
